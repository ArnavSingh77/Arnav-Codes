fruits = ["Watermelon", "Guava", "Strawberry","Mango","Apple"]

#Append
fruits.append("Pear")
print("Append: ", fruits)
print("")

#Insert
fruits.insert(2,"not")
print("Insert: ", fruits)
print("")

#Pop
fruits.pop(2)
print("Pop: ", fruits)
print("")

#Del
del fruits[1]
print("Del: ", fruits)
print("")

#Sort
fruits.sort()
print("Sort: ",fruits)
print("")

#Reverse
fruits.reverse()
print("Reversed: ",fruits)
print("")
