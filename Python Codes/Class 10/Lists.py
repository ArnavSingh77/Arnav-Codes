a = ["arnav", "is", "a","good","boy"]

#Append
a.append(":)")
print("Append: ", a)
print("")

#Insert
a.insert(2,"not")
print("Insert: ", a)
print("")

#Pop
a.pop(2)
print("Pop: ", a)
print("")

#Del
del a[1]
print("Del: ", a)
print("")

#Sort
a.sort()
print("Sort: ",a)
print("")

#Reverse
a.reverse()
print("Reversed: ",a)
print("")